function ifelsecondition(value)
{
    
    if(value=='A')
    {
        console.log("Apple");
    }
    else if(value=='B')
    {
        console.log("Banana");
    }
    else{
        console.log("I don't know");
    }
}
ifelsecondition('A');
ifelsecondition('B');
ifelsecondition('C');